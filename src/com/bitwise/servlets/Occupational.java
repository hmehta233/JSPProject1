package com.bitwise.servlets;

public class Occupational {
	
		private String company;
		private String compadd;
		private String salary;
		private String compcity;
		private String experience;

		public String getCompany() {
			return company;
		}
		public void setCompany(String company) {
			this.company = company;
		}
		public String getCompadd() {
			return compadd;
		}
		public void setCompadd(String compadd) {
			this.compadd = compadd;
		}
		public String getCompcity() {
			return compcity;
		}
		public void setCompcity(String compcity) {
			this.compcity = compcity;
		}
		public String getSalary() {
			return salary;
		}
		public void setSalary(String landmark) {
			this.salary = landmark;
		}
		public String getExperience() {
			return experience;
		}
		public void setExperience(String experience) {
			this.experience = experience;
		}

}

package com.bitwise.servlets;

public class EducationalDetails {
	private String college; 
	private String course;
	private String sscmarks;
	private String hsmarks;
	private String gradmarks;
	
	public String getCollege() {
		return college;
	}
	public void setCollege(String college) {
		this.college = college;
	}
	public String getCourse() {
		return course;
	}
	public void setCourse(String course) {
		this.course = course;
	}
	public String getSscmarks() {
		return sscmarks;
	}
	public void setSsmarks(String sscmarks) {
		this.sscmarks = sscmarks;
	}
	public String getHsmarks() {
		return hsmarks;
	}
	public void setHsmarks(String hsmarks) {
		this.hsmarks = hsmarks;
	}
	public String getGradmarks() {
		return gradmarks;
	}
	public void setGradmarks(String gradmarks) {
		this.gradmarks = gradmarks;
	}
	
}
